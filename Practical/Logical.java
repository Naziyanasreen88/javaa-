package Logical;

public class Logical {

	public static void main(String[] args) {
		boolean a = true, b = false;
				
		//Logical AND
		System.out.println("a && b = " + (a && b));
		
		//Logical AND
		System.out.println("a || b = " + (a || b));
		
		//Logical NOT
		System.out.println("!a && b) = " + ! (a && b));

	}

}
